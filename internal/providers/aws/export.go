// dropNestedBlock removes a `<name> { … }` nested block (brace-balanced, so it
// handles blocks that themselves contain nested blocks) from a block body.
func dropNestedBlock(body []string, name string) []string {
	out := make([]string, 0, len(body))
	for i := 0; i < len(body); i++ {
		if strings.TrimSpace(body[i]) == name+" {" {
			depth, j := 1, i+1
			for ; j < len(body) && depth > 0; j++ {
				depth += strings.Count(body[j], "{") - strings.Count(body[j], "}")
			}
			i = j - 1 // outer loop's i++ lands past the closing brace
			continue
		}
		out = append(out, body[i])
	}
	return out
}

func dropLines(lines []string, re *regexp.Regexp) []string {
	out := lines[:0]
	for _, l := range lines {
		if !re.MatchString(l) {
			out = append(out, l)
		}
	}
	return out
}

// sameAccountPeer reports whether a peering block is same-account (its peer_owner_id
// matches the caller, or is absent — which defaults to the caller's account).
func sameAccountPeer(body []string, account string) bool {
	for _, l := range body {
		if strings.Contains(l, "peer_owner_id") {
			return account != "" && strings.Contains(l, `"`+account+`"`)
		}
	}
	return true // no peer_owner_id -> same account by default
}

func dropEmptyBlocks(lines []string) []string {
	out := make([]string, 0, len(lines))
	for i := 0; i < len(lines); i++ {
		t := strings.TrimSpace(lines[i])
		if name := strings.TrimSuffix(t, " {"); strings.HasSuffix(t, "{") {
			if req, ok := emptyBlockReq[name]; ok {
				j := i + 1
				has := false
				for ; j < len(lines) && strings.TrimSpace(lines[j]) != "}"; j++ {
					if strings.Contains(lines[j], req) {
						has = true
					}
				}
				if !has {
					i = j // skip the whole block including its closing "}"
					continue
				}
			}
		}
		out = append(out, lines[i])
	}
	return out
}

// conflictingBlocks are nested blocks generate-config-out emits ALONGSIDE a
// mutually-exclusive sibling it also emits: an aws_lb gets both `subnet_mapping {…}`
// blocks AND `subnets = [...]`, which conflict ("only one of … can be specified").
// subnets is the simpler/authoritative form, so the subnet_mapping blocks are dropped.
var conflictingBlocks = map[string]bool{"subnet_mapping": true}

// dropOverEmitLists removes every conflicting nested block (flat body, closed by the
// first `}` at its level) that generate-config-out over-emits.
func dropOverEmitLists(lines []string) []string {
	out := make([]string, 0, len(lines))
	for i := 0; i < len(lines); i++ {
		t := strings.TrimSpace(lines[i])
		if name := strings.TrimSpace(strings.TrimSuffix(t, "{")); strings.HasSuffix(t, "{") && conflictingBlocks[name] {
			for i++; i < len(lines) && strings.TrimSpace(lines[i]) != "}"; i++ {
			}
			continue // skip block including its closing "}"
		}
		out = append(out, lines[i])
	}
	return out
}

// hclTagsBlock renders a resource's tags as an indented HCL `tags = { … }` block
// (keys sorted for deterministic output), or "" when there are none. Used by
// natively-authored resources so an import plan doesn't propose stripping live tags.
func hclTagsBlock(tags map[string]string, indent string) string {
	if len(tags) == 0 {
		return ""
	}
	keys := make([]string, 0, len(tags))
	for k := range tags {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	var b strings.Builder
	fmt.Fprintf(&b, "%stags = {\n", indent)
	for _, k := range keys {
		// Escape ${ / %{ so an API-supplied tag key or value can't inject a Terraform
		// template expression (e.g. a tag literally named `${file("~/.aws/credentials")}`).
		fmt.Fprintf(&b, "%s  %q = %q\n", indent, util.EscapeHCLTemplate(k), util.EscapeHCLTemplate(tags[k]))
	}
	fmt.Fprintf(&b, "%s}\n", indent)
	return b.String()
}

// authorKeyPairs authors aws_key_pair resources directly. generate-config-out
// drops them (public_key is write-only in the provider schema, so it can't read it
// back), leaving them a coverage gap and any instance's key_name reference dangling.
// Resource Explorer reports a key pair by its ID (key-…), not its name, so fetch by
// --key-pair-ids to get the real KeyName + PublicKey, author a real block, register
// the KeyName for cross-reference rewiring, and correct the import id (a key pair
// imports by name, not id). Returns the count authored.
func authorKeyPairs(ctx context.Context, path, region string, items []*model.Resource, addrs []string, crossRef map[string][]refTarget) int {
	declared := declaredAddrs(path)
	var b strings.Builder
	n := 0
	for i, r := range items {
		if r.NativeType != "ec2:key-pair" || declared[addrs[i]] {
			continue // skip if generate-config-out already emitted it (avoid a duplicate)
		}
		reg := r.Location
		if reg == "" {
			reg = region
		}
		var kp struct{ Name, Pub string }
		_ = runAws(ctx, &kp, "ec2", "describe-key-pairs", "--key-pair-ids", r.Name,
			"--include-public-key", "--region", reg, "--query", "KeyPairs[0].{Name:KeyName,Pub:PublicKey}")
		if kp.Name == "" || kp.Pub == "" {
			continue
		}
		label := addrs[i]
		if dot := strings.LastIndex(label, "."); dot >= 0 {
			label = label[dot+1:]
		}
		// public_key is write-only: an import doesn't read it back, so without
		// ignore_changes the config value "forces replacement" on adoption. Ignored on
		// import (no replace); still used on a from-scratch create. Tags come from the
		// enumerated resource so an import plan doesn't propose stripping the live tags.
		// key_name is an API string (≤255 arbitrary chars) — escape ${ / %{ so it can't
		// inject a Terraform template expression evaluated at plan time.
		fmt.Fprintf(&b, "\nresource \"aws_key_pair\" %q {\n  key_name   = %q\n  public_key = %q\n%s  lifecycle { ignore_changes = [public_key] }\n}\n", label, util.EscapeHCLTemplate(kp.Name), strings.TrimSpace(kp.Pub), hclTagsBlock(r.Tags, "  "))
		crossRef["aws_key_pair"] = append(crossRef["aws_key_pair"], refTarget{addr: addrs[i], name: kp.Name})
		if r.Properties == nil {
			r.Properties = map[string]any{}
		}
		r.Properties["tl_keyname"] = kp.Name // so the reconcile's deriveImportID imports by name
		n++
	}
	if n > 0 {
		if f, err := os.OpenFile(path, os.O_APPEND|os.O_WRONLY, 0o644); err == nil {
			_, _ = f.WriteString(b.String())
			_ = f.Close()
		}
	}
	return n
}

// authorListeners authors aws_lb_listener resources directly. generate-config-out
// drops them (a gap), which leaves a target group with no load-balancer association
// so any ECS service using it fails to create. describe-listeners returns the
// config; the load_balancer_arn / target_group_arn are emitted literally and later
// rewired to references. Returns the authored listener addresses.
func authorListeners(ctx context.Context, path, region string, items []*model.Resource, addrs []string) []string {
	declared := declaredAddrs(path)
	var b strings.Builder
	var authored []string
	for i, r := range items {
		if !strings.HasPrefix(r.NativeType, "elasticloadbalancing:listener") || declared[addrs[i]] {
			continue
		}
		reg := r.Location
		if reg == "" {
			reg = region
		}
		var l struct {
			Port            int    `json:"Port"`
			Protocol        string `json:"Protocol"`
			LoadBalancerArn string `json:"LoadBalancerArn"`
			DefaultActions  []struct {
				Type           string `json:"Type"`
				TargetGroupArn string `json:"TargetGroupArn"`
			} `json:"DefaultActions"`
		}
		if err := runAws(ctx, &l, "elbv2", "describe-listeners", "--listener-arns", r.ID, "--region", reg, "--query", "Listeners[0]"); err != nil || l.LoadBalancerArn == "" {
			continue
		}
		label := addrs[i]
		if dot := strings.LastIndex(label, "."); dot >= 0 {
			label = label[dot+1:]
		}
		fmt.Fprintf(&b, "\nresource \"aws_lb_listener\" %q {\n  load_balancer_arn = %q\n  port     = %d\n  protocol = %q\n", label, l.LoadBalancerArn, l.Port, l.Protocol)
		for _, a := range l.DefaultActions {
			if a.Type == "forward" && a.TargetGroupArn != "" {
				fmt.Fprintf(&b, "  default_action {\n    type             = \"forward\"\n    target_group_arn = %q\n  }\n", a.TargetGroupArn)
			}
		}
		b.WriteString("}\n")
		authored = append(authored, addrs[i])
	}
	if len(authored) > 0 {
		if f, err := os.OpenFile(path, os.O_APPEND|os.O_WRONLY, 0o644); err == nil {
			_, _ = f.WriteString(b.String())
			_ = f.Close()
		}
	}
	return authored
}

// addEcsListenerDeps makes every aws_ecs_service that attaches to a load balancer
// depend on all authored listeners, so the target-group→LB association exists before
// the service is created ("target group does not have an associated load balancer").
func addEcsListenerDeps(path string, listeners []string) {
	if len(listeners) == 0 {
		return
	}
	data, err := os.ReadFile(path)
	if err != nil {
		return
	}
	dep := "  depends_on = [" + strings.Join(listeners, ", ") + "]\n"
	lines := strings.Split(string(data), "\n")
	var out []string
	for i := 0; i < len(lines); i++ {
		m := resHeaderRe.FindStringSubmatch(lines[i])
		if m == nil || m[1] != "aws_ecs_service" {
			out = append(out, lines[i])
			continue
		}
		depth, end := hcl.BraceDelta(lines[i]), i
		for j := i + 1; j < len(lines); j++ {
			depth += hcl.BraceDelta(lines[j])
			if depth <= 0 {
				end = j
				break
			}
		}
		block := lines[i:end] // header .. line before closing brace
		if anyMatches(block, regexp.MustCompile(`load_balancer\s*\{`)) {
			out = append(out, block...)
			out = append(out, strings.TrimRight(dep, "\n"))
			out = append(out, lines[end])
		} else {
			out = append(out, lines[i:end+1]...)
		}
		i = end
	}
	_ = os.WriteFile(path, []byte(strings.Join(out, "\n")), 0o644)
}

// authorTaskDefs authors aws_ecs_task_definition resources directly.
// generate-config-out cannot emit them (it drops them to a gap), which dangles any
// ECS service's task_definition reference. describe-task-definition returns the full
// definition, so re-author it (control-plane: container_definitions may carry env
// config, which ships and is flagged by the secrets review, per ADR-001). The
// import id is the task-definition ARN. Returns the count authored.
func authorTaskDefs(ctx context.Context, path, region string, items []*model.Resource, addrs []string) int {
	declared := declaredAddrs(path)
	var b strings.Builder
	n := 0
	for i, r := range items {
		if r.NativeType != "ecs:task-definition" || declared[addrs[i]] {
			continue // skip if generate-config-out already emitted it (avoid a duplicate)
		}
		reg := r.Location
		if reg == "" {
			reg = region
		}
		var td struct {
			Family                  string          `json:"family"`
			TaskRoleArn             string          `json:"taskRoleArn"`
			ExecutionRoleArn        string          `json:"executionRoleArn"`
			NetworkMode             string          `json:"networkMode"`
			Cpu                     string          `json:"cpu"`
			Memory                  string          `json:"memory"`
			RequiresCompatibilities []string        `json:"requiresCompatibilities"`
			ContainerDefinitions    json.RawMessage `json:"containerDefinitions"`
		}
		if err := runAws(ctx, &td, "ecs", "describe-task-definition", "--task-definition", r.Name, "--region", reg, "--query", "taskDefinition"); err != nil || td.Family == "" {
			continue
		}
		var compact bytes.Buffer
		if json.Compact(&compact, td.ContainerDefinitions) != nil {
			continue
		}
		// Escape Terraform interpolation sequences so an env value like "${X}" in the
		// JSON is treated literally inside the heredoc.
		cdefs := strings.ReplaceAll(strings.ReplaceAll(compact.String(), "${", "$${"), "%{", "%%{")

		label := addrs[i]
		if dot := strings.LastIndex(label, "."); dot >= 0 {
			label = label[dot+1:]
		}
		fmt.Fprintf(&b, "\nresource \"aws_ecs_task_definition\" %q {\n  family = %q\n", label, td.Family)
		if td.NetworkMode != "" {
			fmt.Fprintf(&b, "  network_mode = %q\n", td.NetworkMode)
		}
		if len(td.RequiresCompatibilities) > 0 {
			fmt.Fprintf(&b, "  requires_compatibilities = %q\n", td.RequiresCompatibilities)
		}
		if td.Cpu != "" {
			fmt.Fprintf(&b, "  cpu = %q\n", td.Cpu)
		}
		if td.Memory != "" {
			fmt.Fprintf(&b, "  memory = %q\n", td.Memory)
		}
		if td.ExecutionRoleArn != "" {
			fmt.Fprintf(&b, "  execution_role_arn = %q\n", td.ExecutionRoleArn)
		}
		if td.TaskRoleArn != "" {
			fmt.Fprintf(&b, "  task_role_arn = %q\n", td.TaskRoleArn)
		}
		fmt.Fprintf(&b, "  container_definitions = <<-CONTAINERS\n  %s\n  CONTAINERS\n}\n", cdefs)
		n++
	}
	if n > 0 {
		if f, err := os.OpenFile(path, os.O_APPEND|os.O_WRONLY, 0o644); err == nil {
			_, _ = f.WriteString(b.String())
			_ = f.Close()
		}
	}
	return n
}

// declaredAddrs returns the "type.name" addresses already present in the file, so
// the native authors don't emit a duplicate of a resource generate-config-out did
// manage to produce (its output is not fully deterministic across runs).
func declaredAddrs(path string) map[string]bool {
	out := map[string]bool{}
	data, err := os.ReadFile(path)
	if err != nil {
		return out
	}
	for _, m := range resourceLabelRe.FindAllStringSubmatch(string(data), -1) {
		out[m[1]+"."+m[2]] = true
	}
	return out
}

// refTarget is one exported resource, indexed so cross-references to it can be
// rewired to a Terraform reference (needed for greenfield re-apply: literal
// names/ARNs/revisions don't exist after a destroy/rebuild).
type refTarget struct {
	addr, name, id, arn string
}

// crossRefRule rewrites a specific reference ATTRIBUTE (never a bare name, which
// would self-reference a resource's own `name`) to the target resource's attribute.
type crossRefRule struct {
	attr, targetType, targetAttr string
	re                           *regexp.Regexp // compiled once (init); matches `attr = "…"`
}

var awsCrossRefRules = []crossRefRule{
	{attr: "task_definition", targetType: "aws_ecs_task_definition", targetAttr: "arn"},
	{attr: "event_bus_name", targetType: "aws_cloudwatch_event_bus", targetAttr: "name"},
	{attr: "key_name", targetType: "aws_key_pair", targetAttr: "key_name"},
	{attr: "key_id", targetType: "aws_kms_key", targetAttr: "key_id"},
	{attr: "kms_key_id", targetType: "aws_kms_key", targetAttr: "arn"},
	{attr: "kms_master_key_id", targetType: "aws_kms_key", targetAttr: "arn"},
	{attr: "target_group_arn", targetType: "aws_lb_target_group", targetAttr: "arn"},
	{attr: "load_balancer_arn", targetType: "aws_lb", targetAttr: "arn"},
}

func init() { // precompile each rule's line matcher once, not per (line × rule)
	for i := range awsCrossRefRules {
		r := &awsCrossRefRules[i]
		r.re = regexp.MustCompile(`^(\s*)` + regexp.QuoteMeta(r.attr) + `(\s*)=\s*"([^"]+)"\s*(#.*)?$`)
	}
}

// rewireCrossRefs rewrites literal cross-resource references (by name, ARN, or
// name:revision) to Terraform references, so the repo re-applies from scratch with
// correct ordering and post-rebuild ids. Attribute-scoped so a resource's own
// `name = "x"` is never turned into a self-reference. Returns the count rewired.
func rewireCrossRefs(path string, byType map[string][]refTarget) int {
	data, err := os.ReadFile(path)
	if err != nil {
		return 0
	}
	src := string(data)
	// Only rewire to a target whose resource block is ACTUALLY DECLARED here: a
	// referenced resource that generate-config-out dropped to a gap (e.g. an ECS
	// task definition it couldn't emit) is not declared, and pointing at it yields
	// "Reference to undeclared resource".
	declared := map[string]bool{}
	for _, m := range resourceLabelRe.FindAllStringSubmatch(src, -1) {
		declared[m[1]+"."+m[2]] = true
	}
	lines := strings.Split(src, "\n")
	n := 0
	curType := "" // resource type of the block being scanned
	for i, l := range lines {
		if m := resHeaderRe.FindStringSubmatch(l); m != nil {
			curType = m[1]
			continue
		}
		for _, rule := range awsCrossRefRules {
			if curType == rule.targetType {
				continue // inside the target type itself: `key_name` here is its OWN name, not a reference
			}
			m := rule.re.FindStringSubmatch(l)
			if m == nil {
				continue
			}
			t := matchTarget(byType[rule.targetType], m[3])
			if t == nil || !declared[t.addr] {
				break // no exported+declared target (AWS-managed key, or a gapped resource) — leave literal
			}
			lines[i] = m[1] + rule.attr + m[2] + "= " + t.addr + "." + rule.targetAttr
			n++
			break
		}
	}
	if n > 0 {
		_ = os.WriteFile(path, []byte(strings.Join(lines, "\n")), 0o644)
	}
	return n
}

// matchTarget resolves a literal reference value to an exported resource: exact
// name, exact id/ARN, or a "name:revision" whose name matches (ECS task defs).
func matchTarget(targets []refTarget, val string) *refTarget {
	nameOnly := val
	if i := strings.Index(val, ":"); i > 0 && !strings.HasPrefix(val, "arn:") {
		nameOnly = val[:i] // "family:revision" -> "family"
	}
	for i := range targets {
		t := &targets[i]
		if val == t.name || val == t.id || val == t.arn || nameOnly == t.name {
			return t
		}
	}
	return nil
}

func anyMatches(lines []string, re *regexp.Regexp) bool {
	for _, l := range lines {
		if re.MatchString(l) {
			return true
		}
	}
	return false
}

func tail(s string, lines int) string {
	parts := strings.Split(strings.TrimRight(s, "\n"), "\n")
	if len(parts) > lines {
		parts = parts[len(parts)-lines:]
	}
	return strings.Join(parts, "\n")
}
